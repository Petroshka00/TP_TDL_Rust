pub fn draw_ui(ecs: &World, ctx : &mut Rltk) {
    let box_upper_limit = SCREEN_HEIGHT - GUI_HEIGHT - 1;
    ctx.draw_box(0, box_upper_limit, GUI_WIDTH - 1, GUI_HEIGHT, RGB::named(rltk::WHITE), RGB::named(rltk::BLACK));

    let log = ecs.fetch::<GameLog>();

    let mut y = SCREEN_HEIGHT - GUI_HEIGHT + 1;
    for s in log.entries.iter().rev() {
        if y < SCREEN_HEIGHT - 1 { ctx.print(2, y, s); }
        y += 1;
    }

    let map = ecs.fetch::<Map>();
    let depth = format!("Piso: {}", map.depth);
    ctx.print_color(2, box_upper_limit, RGB::named(rltk::YELLOW), RGB::named(rltk::BLACK), &depth);

    let combat_stats = ecs.read_storage::<CombatStats>();
    let players = ecs.read_storage::<Player>();
    for (_player, stats) in (&players, &combat_stats).join() {
        let health = format!(" HP: {} / {} ", stats.hp, stats.max_hp);
        ctx.print_color(SCREEN_WIDTH / 8, SCREEN_HEIGHT - GUI_HEIGHT - 1, RGB::named(rltk::YELLOW), RGB::named(rltk::BLACK), &health);

        ctx.draw_bar_horizontal(SCREEN_WIDTH / 4, SCREEN_HEIGHT - GUI_HEIGHT - 1, 51, stats.hp, stats.max_hp, RGB::named(rltk::RED), RGB::named(rltk::BLACK));
    }
}

#[derive(PartialEq, Copy, Clone)]
pub enum GameOverResult { NoSelection, QuitToMenu }

pub fn game_over(ctx : &mut Rltk) -> GameOverResult {
    ctx.print_color_centered(15, RGB::named(rltk::YELLOW), RGB::named(rltk::BLACK), "Tu viaje ha terminado!");
    ctx.print_color_centered(17, RGB::named(rltk::WHITE), RGB::named(rltk::BLACK), "Mejor suerte para la proxima");
    ctx.print_color_centered(18, RGB::named(rltk::WHITE), RGB::named(rltk::BLACK), "Seguro podras mejorar tu puntaje!");

    ctx.print_color_centered(20, RGB::named(rltk::MAGENTA), RGB::named(rltk::BLACK), "Presiona cualquier tecla para volver al menu principal");

    match ctx.key {
        None => GameOverResult::NoSelection,
        Some(_) => GameOverResult::QuitToMenu
    }
}